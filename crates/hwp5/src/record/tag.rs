pub const SHAPE_COMPONENT_LINE: u16 = HWPTAG_BEGIN + 62;
pub const SHAPE_COMPONENT_RECTANGLE: u16 = HWPTAG_BEGIN + 63;
pub const SHAPE_COMPONENT_ELLIPSE: u16 = HWPTAG_BEGIN + 64;
