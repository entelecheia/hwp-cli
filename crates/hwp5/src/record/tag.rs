//! 레코드 태그 상수와 이름 조회.
//!
//! 태그는 **항상 원시 u16으로 보존**한다 — enum 강제 변환을 하지 않아
//! 새 태그가 등장해도 정보가 깨지지 않는다. 이 모듈은 디버깅/덤프용
//! 이름 조회만 제공한다. (한글문서파일형식 5.0 §3 데이터 레코드)

pub const HWPTAG_BEGIN: u16 = 0x010;

// ── DocInfo 레코드 ──────────────────────────────────────────────
pub const DOCUMENT_PROPERTIES: u16 = HWPTAG_BEGIN; // 0x10
pub const ID_MAPPINGS: u16 = HWPTAG_BEGIN + 1;
pub const BIN_DATA: u16 = HWPTAG_BEGIN + 2;
